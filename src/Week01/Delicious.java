package Week01;
// 양꼬치
public class Delicious {
    public int solution(int n, int k) {
        int answer = 0;
        int dis = n/10;
		
        return answer = 12000 * n + 2000 * (k-dis);
    }
    // 출력
    public static void main(String[] args) {
    	Delicious a = new Delicious();
		
    	System.out.println(a.solution(64, 6));
	}
}
